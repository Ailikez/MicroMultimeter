#include "drivers\stm32f3_system.h"

#include "uMultimeter.h"
/*====================================================================================================*/
/*====================================================================================================*/
void UM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  UM_GPIO_Config();
  UM_TIM15_PWM_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void UM_Loop( void )
{
  while(1) {
    LED_G_Toggle();
    delay_ms(100);
  }
}
