WaveForm_Struct WaveForm;
/*====================================================================================================*/
/*====================================================================================================*/
void UM_Init( void )
{
  UM_BSP_GPIO_Init();
  SSD1331_Config();
  delay_ms(100);

  SSD1331_Init();

	WaveForm.Channel       = 2;
	WaveForm.WindowColor   = WHITE;
	WaveForm.BackColor     = BLACK;
	WaveForm.Data[0]       = 0;
	WaveForm.Data[1]       = 0;
	WaveForm.Scale[0]      = 100;
	WaveForm.Scale[1]      = 100;
	WaveForm.PointColor[0] = GREEN;
	WaveForm.PointColor[1] = BLUE;

  OLED_Clear(BLACK);
}
